package Polymorpism1;

public class Person {

	String name;
	int age;

	public Person(String name, int age) {
		super();
		this.name = name;
		this.age = age;

	}

	public String getname() {
		return name;
	}

	public void setname(String name) {
		name = name;

	}

	public int getage() {
		return age;
	}

	public void setage(int age) {
		age = age;

	}

}